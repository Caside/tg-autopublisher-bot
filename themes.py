"""
Модуль для управления темами постов.
Темы могут быть загружены из переменных окружения или использованы по умолчанию.
"""

import os
import json

# Темы по умолчанию
DEFAULT_THEMES = [
    "психическое благополучие",
    "проблемы чрезмерного использования ИИ в жизни людей",
    "различия между целями и смыслом жизни",
    "здоровый цинизм и кризис идентичности",
    "проблема замещения личных мыслей на нарративы от лидеров общественного мнения",
    "проблемы коллективного бессознательного",
    "принятие неидеальности как уникального способа существования",
    "влияние капитализма на тревогу и стресс",
    "экономика человеческого внимания",
    "инфантилизм среди взрослых в современном мире"
]

def get_themes():
    """
    Получает темы из переменной окружения POST_THEMES или возвращает темы по умолчанию.
    Если POST_THEMES установлена, она должна быть JSON-строкой с массивом тем.
    """
    themes_env = os.getenv('POST_THEMES')
    if themes_env:
        try:
            return json.loads(themes_env)
        except json.JSONDecodeError:
            print("Ошибка: POST_THEMES содержит некорректный JSON. Используются темы по умолчанию.")
            return DEFAULT_THEMES
    return DEFAULT_THEMES

# Экспортируем список тем
POST_THEMES = get_themes() 