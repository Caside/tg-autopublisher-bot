"""
Модуль для управления темами постов.
Темы могут быть загружены из переменных окружения или использованы по умолчанию.
"""

import os
import json

# Темы по умолчанию
DEFAULT_THEMES = [
    "если ИИ создали и обучили люди, то является ли он искуственным?",
    "а что если ты на самом деле ИИ а не человек?",
    "психологический возраст коллективного бессознательного",
    "критическое мышление в цифровую эпоху",
    "игра в смыслы: как мы симулируем важность происходящего",
    "проблемы идентичности в постмодерне и метамодерне",
    "кто ты без всех социальных масок и ролей?",
    "современный шаманизм и бытовая магия в цифровую эпоху",
    "проблемы в определении сознания",
    "ретроспективная псевдоностальгия и отсутствие образа будущего"
    ]

def get_themes():
    """
    Получает темы из переменной окружения POST_THEMES или возвращает темы по умолчанию.
    Если POST_THEMES установлена, она должна быть JSON-строкой с массивом тем.
    """
    themes_env = os.getenv('POST_THEMES')
    if themes_env:
        try:
            return json.loads(themes_env)
        except json.JSONDecodeError:
            print("Ошибка: POST_THEMES содержит некорректный JSON. Используются темы по умолчанию.")
            return DEFAULT_THEMES
    return DEFAULT_THEMES

# Экспортируем список тем
POST_THEMES = get_themes() 