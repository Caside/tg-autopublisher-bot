"""
Модуль для управления темами постов.
Темы могут быть загружены из переменных окружения или использованы по умолчанию.
"""

import os
import json

# Темы по умолчанию
DEFAULT_THEMES = [
    "усталость от бесконечного самосовершенствования",
    "парадокс свободы в эпоху выбора из миллиона вариантов",
    "эмоциональное выгорание как форма культурной адаптации",
    "отчуждение в обществе, где все постоянно на связи",
    "зависимость от чужих реакций и исчезновение собственного «я»",
    "игра в смыслы: как мы симулируем важность происходящего",
    "медленное разрушение самоощущения через бесконечную прокрутку контента",
    "ощущение фальши в мире нескончаемых советов будь собой",
    "абсурд взросления без инициации и ритуалов",
    "жажда тишины в шумной культуре объяснений"]

def get_themes():
    """
    Получает темы из переменной окружения POST_THEMES или возвращает темы по умолчанию.
    Если POST_THEMES установлена, она должна быть JSON-строкой с массивом тем.
    """
    themes_env = os.getenv('POST_THEMES')
    if themes_env:
        try:
            return json.loads(themes_env)
        except json.JSONDecodeError:
            print("Ошибка: POST_THEMES содержит некорректный JSON. Используются темы по умолчанию.")
            return DEFAULT_THEMES
    return DEFAULT_THEMES

# Экспортируем список тем
POST_THEMES = get_themes() 